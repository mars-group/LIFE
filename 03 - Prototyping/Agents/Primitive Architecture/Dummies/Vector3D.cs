namespace Primitive_Architecture.Dummies {

  /// <summary>
  /// This is a simple structure to represent a coordinate or vector in a 3D space.
  /// </summary>
  public struct Vector3D {
    public float X; // Planar value 1, x-axis (left/right) 
    public float Y; // Planar value 2, y-axis (forward/backward)
    public float Z; // Height value (up/down)
  }
}